CREATE TABLE DEPARTMENTS ( 
    DEPARTMENTS_DEPT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
    DEPARTMENTS_NAME VARCHAR2(100) NOT NULL 
);

CREATE TABLE STUDENTS ( 
    STUDENT_ID NUMBER PRIMARY KEY, 
    STUDENTS_FIRST_NAME VARCHAR2(100) NOT NULL, 
    STUDENTS_LAST_NAME VARCHAR2(100) NOT NULL, 
    STUDENTS_EMAIL VARCHAR2(150) UNIQUE NOT NULL, 
    STUDENTS_DEPT_ID NUMBER NOT NULL, 
    STUDENTS_JOINING_DATE DATE, 
    FOREIGN KEY (STUDENTS_DEPT_ID) REFERENCES DEPARTMENTS(DEPARTMENTS_DEPT_ID) 
);

CREATE TABLE SUBJECTS ( 
    SUBJECT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
    SUBJECT_NAME VARCHAR2(150) NOT NULL, 
    SUBJECT_DEPT_ID NUMBER NOT NULL, 
    FOREIGN KEY (SUBJECT_DEPT_ID) REFERENCES DEPARTMENTS (DEPARTMENTS_DEPT_ID) 
);


CREATE TABLE MARKS ( 
    MARKS_STUDENT_ID NUMBER NOT NULL, 
    MARKS_SUBJECT_ID NUMBER NOT NULL, 
    MARKS_SCORED NUMBER CHECK (marks BETWEEN 0 AND 100), 
    PRIMARY KEY (STUDENT_ID, SUBJECT_ID), 
    FOREIGN KEY (MARKS_STUDENT_ID) REFERENCES STUDENTS(STUDENT_ID), 
    FOREIGN KEY (MARKS_SUBJECT_ID) REFERENCES SUBJECTS(SUBJECT_ID) 
);

CREATE TABLE GRADES ( 
    GRADES_ID NUMBER PRIMARY KEY, 
    GRADES_CODE VARCHAR2(10) NOT NULL, 
    GRADES_LABEL VARCHAR2(50) NOT NULL, 
    GRADES_PERCENTAGE_RANGE VARCHAR2(20) NOT NULL, 
    GRADES_GPA_EQUIVALENT NUMBER(2,1) NOT NULL 
);

